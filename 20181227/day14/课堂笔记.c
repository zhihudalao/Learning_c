/*
动态内存分配:
	数组扩容功能。由于数组扩容功能肯定是调用函数去完成的。
	所谓数组扩容，肯定是当发现旧的数组容量不够时，则在函数内部声明一个新的更大的数组，然后将旧的数组内的内容复制过去。
	最后一步就是将新的数组返回到外部，外部用一个数组去接受，然后接下来所有操作应该是针对新的数组进行操作。
	由于这里在函数内部申请的指针是一个栈空间的指针，当函数结束时，指针指向的空间被系统回收，所以，即使返回到外部，外部也无法使用。
	所以，要解决这个动态内存分配问题，无法使用栈空间的指针来实现，只能使用堆空间的指针来实现
*/
/*
	堆空间指针的特点
		①随机访问
		②手动申请
		③手动释放
	如何为一个指针变量申请堆空间内存：
		当一个指针变量直接申请的时候，则系统默认分配内存空间，此时在栈空间上
		当一个指针变量是用malloc函数申请的空间时，则此时该指针在堆空间上
			#这里的malloc就是所谓的手动申请
*/
/*
	malloc()的用法
	函数名：malloc
	函数功能：为一个指针变量申请一片堆空间的内存
	函数参数列表：1个参数，代表需要为这个指针变量申请多少大小的内存空间地址
	返回值：void*（为什么返回值是通用指针：用为malloc可以为所有类型的指针申请堆空间内存，包括但不限于int*,char*,stu_t*，所以肯定需要是一个通用指针，能够去接受所有诶性的指针）
*/
/*
	memset()的用法
	memset(DB,0,sizeof(stu_t)*vol);
	函数名：memset
	函数功能：给一个指针所指向的空间全部 初始化/赋值
	函数参数列表：3个：①代表需要初始化/赋值的指针变量
			②代表需要初始化/赋值的确切的值
			③代表需要给这个指针指向的空间内多少大小的空间进行初始化/赋值
	#malloc和memset几乎都是配套使用，不会形单影只
*/
/*
	calloc（）函数的用法
	函数名：calloc
	函数功能：给一个堆空间的指针，申请指定大小的内存空间，并且同时给这片内存空间全都初始化为0
	函数参数列表：2个：
			①代表需要申请多少块内存空间
			②每一块内存空间有多少大
			#那么，其意味着，申请的总空间大小就是2个参数之积
			
	当需要申请一片连续的堆空间内存是（视为堆空间数组）
	使用malloc的话：stu_t* DB=(stu_t*)malloc(sizeof(stu)*n);
			memset(DB,0,sizeof(stu)*n);
	如果是使用calloc的话：stu_t* DB=(stu_t*)calloc(n,sizeof(stu));
	#calloc其实就是malloc的优化版函数。除了以上使用方法的简化外，申请后的堆空间内存操作方法完全一致
*/
/*
	realloc()的用法
	函数名：realloc
	函数功能：为传入的指针重新申请一片内存空间大小
	函数参数列表：2个：
			①代表需要重新开辟内存空间的指针
			②需要重新开辟的内存空间的大小
	函数返回值：void*
*/

